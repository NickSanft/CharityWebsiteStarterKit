#!/usr/bin/env node
import { copyFileSync, mkdirSync, existsSync, readdirSync, statSync, rmSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const SITE = join(ROOT, 'builder-site');
const SITE_SRC = join(SITE, 'src');

/**
 * Files and directories that must be copied into builder-site/src/ so the
 * sub-project can be built in isolation (with turbopack.root pinned there).
 * Paths are relative to the repo root and preserved under builder-site/src/.
 */
const SYNC = [
  'src/lib/builder',
  'src/lib/utils.ts',
  'src/components/ui/button.tsx',
  'src/components/ui/input.tsx',
  'src/components/ui/label.tsx',
  'src/components/ui/textarea.tsx',
  'src/components/ui/checkbox.tsx',
  'src/app/builder',
];

function copyRecursive(src, dest) {
  const s = statSync(src);
  if (s.isDirectory()) {
    mkdirSync(dest, { recursive: true });
    for (const entry of readdirSync(src)) {
      copyRecursive(join(src, entry), join(dest, entry));
    }
  } else {
    mkdirSync(dirname(dest), { recursive: true });
    copyFileSync(src, dest);
  }
}

// Wipe previously-synced code so stale files can't linger between runs.
const synced = join(SITE_SRC);
if (existsSync(synced)) rmSync(synced, { recursive: true, force: true });

for (const rel of SYNC) {
  const from = join(ROOT, rel);
  if (!existsSync(from)) {
    console.error(`Missing ${from}; aborting.`);
    process.exit(1);
  }
  const to = join(SITE_SRC, relative(join(ROOT, 'src'), from));
  copyRecursive(from, to);
  console.log(`Synced ${rel}`);
}

// Template manifest (generated by scripts/build-template-manifest.mjs).
const manifestSrc = join(ROOT, 'public', 'builder-template.json');
const manifestDest = join(SITE, 'public', 'builder-template.json');
if (!existsSync(manifestSrc)) {
  console.error(`Missing ${manifestSrc}. Run "npm run builder:manifest" first.`);
  process.exit(1);
}
mkdirSync(dirname(manifestDest), { recursive: true });
copyFileSync(manifestSrc, manifestDest);
console.log('Synced public/builder-template.json');
